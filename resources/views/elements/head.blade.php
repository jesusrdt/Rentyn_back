            dataType: "json",
            type:  'post',
            success:  function (data5) {
                data5['resultado'] = new Array();
                
                var dato = data5[data5.length-1];
                if(dato.Label == 'Media'){
                    $.each(dato.Data[0]['Column 0'], function(keym, media){

                        if(keym == 0 && media.Content_Type != '.jpg'){
                            if(data == true){
                                $("[data-id_maquina_imagenes='" + id_maquina +"']").html('<img src="https://www.rentyn.com/temas/base/images/no-image.jpg">');
                            }else{
                                if(donde != ''){
                                    $(donde).html('<img src="https://www.rentyn.com/temas/base/images/no-image.jpg">');
                                }else{
                                    $('.imagen-multimedia').html('<img src="https://www.rentyn.com/temas/base/images/no-image.jpg">');
                                }
                            }
                        }

                        if(media.Content_Type == '.jpg' && tipo == 'img'){
                            if(data==true){
                                if(limite == 1){
                                    $("[data-id_maquina_imagenes='" + id_maquina +"']").html('<img src="'+url_files+media.Filename+'">');
                                
                                }else{
                                    $("[data-id_maquina_imagenes='" + id_maquina +"']").append('<img src="'+url_files+media.Filename+'">');                                     
                                }                                           
                            }else{
                                if(donde != ''){
                                    if(limite == 1){
                                        $(donde).html('<a class="open-gallery" href="'+url_files+media.Filename+'" data-group="'+id_maquina+'"><img src="'+url_files+media.Filename+'"></a>');
                                    }else{
                                        if(keym == 0){
                                            $(donde).append('<a class="open-gallery" href="'+url_files+media.Filename+'" data-group="'+id_maquina+'"><img src="'+url_files+media.Filename+'"></a>');
                                            $('.centered-gallery').slick('slickAdd','<a class="open-gallery" href="'+url_files+media.Filename+'" data-group="'+id_maquina+'"><img src="'+url_files+media.Filename+'"></a>');
                                        }else{
                                            $('.centered-gallery').slick('slickAdd','<a class="open-gallery" href="'+url_files+media.Filename+'" data-group="'+id_maquina+'"><img src="'+url_files+media.Filename+'"></a>');                            
                                        }
                                    }   
                                }else{ 
                                    $('.imagen-multimedia').html('<img src="'+url_files+media.Filename+'">');
                                }                                           
                            }   
                                                        
                        }else{
                            if(media.Content_Type == '.pdf' && tipo == 'pdf'){
                                if(data===true){
                                    if(limite == 1){
                                        $("[data-id_maquina_pdf='" + id_maquina +"']").html('<div class="pdf"><a href="'+url_files+media.Filename+'" target="_blank">Descargar PDF</a></div>');
                                        
